#!/system/bin/sh

echo "====================================="
echo "   🔥 DEEP BACKGROUND CLEANER 🔥     "
echo "====================================="
echo "[+] جاري بدء عملية التنظيف العميق للرام..."
echo "-------------------------------------"

# 1. إغلاق إجباري لجميع تطبيقات الخلفية
echo "[+] جاري إغلاق تطبيقات الخلفية..."
for proc in $(pm list packages -3 | cut -d':' -f2); do
    # استثناء تطبيق الكيرنل سو وتطبيقات الروت الأساسية
    if [ "$proc" != "io.github.tiann.kernelsu" ] && [ "$proc" != "com.topjohnwu.magisk" ]; then
        am force-stop "$proc"
    fi
done

# 2. تنظيف كاش المعالج والذاكرة المؤقتة
echo "[+] جاري تفريغ الكاش اللحظي (Drop Caches)..."
sync
echo 3 > /proc/sys/vm/drop_caches

# 3. تحرير ما تبقى من مساحة الرام الإضافية
am kill-all

echo "-------------------------------------"
echo "⚡ تم تنظيف الرام بنجاح يا بطل! ⚡"
echo "🎮 تم إخلاء المساحة وجهازك جاهز الآن للألعاب."
echo "====================================="
exit 0
